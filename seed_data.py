def seed_orderbook():
    """Store real orderbook snapshots. Logs spread and depth as latency/issue records
    so the dashboard can display orderbook health."""
    for symbol, book in ORDERBOOKS.items():
        best_ask = book["asks"][0][0]
        best_bid = book["bids"][0][0]
        spread = best_ask - best_bid
        spread_bps = (spread / best_bid) * 10000

        # Sum depth on each side (in base units)
        ask_depth = sum(level[1] for level in book["asks"])
        bid_depth = sum(level[1] for level in book["bids"])

        # Compute notional depth in USD
        mid = (best_ask + best_bid) / 2
        ask_depth_usd = ask_depth * mid
        bid_depth_usd = bid_depth * mid

        # Store as execution_quality record for dashboard visibility
        conn = get_conn()
        conn.execute(
            """INSERT INTO execution_quality
               (ts, symbol, order_type, expected_price, filled_price, slippage_bps, latency_ms, status)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?)""",
            (FETCH_TS, symbol, "ORDERBOOK_SNAPSHOT", best_bid, best_ask,
             round(spread_bps, 2), 0, f"ask_depth_usd={round(ask_depth_usd,2)},bid_depth_usd={round(bid_depth_usd,2)}")
        )
        conn.commit()
        release_conn(conn)

        print(f"  {symbol}: spread={spread:.2f} ({spread_bps:.1f}bps), "
              f"ask_depth=${ask_depth_usd:,.0f}, bid_depth=${bid_depth_usd:,.0f}")


